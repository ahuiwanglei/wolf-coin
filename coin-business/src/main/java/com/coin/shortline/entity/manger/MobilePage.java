package com.coin.shortline.entity.manger;

import java.util.List;

public class MobilePage {
	private long total;
	private List rows;

	public MobilePage(long total, List rows) {
		super();
		this.total = total;
		this.rows = rows;
	}

	public long getTotal() {
		return total;
	}

	public void setTotal(long total) {
		this.total = total;
	}

	public List getRows() {
		return rows;
	}

	public void setRows(List rows) {
		this.rows = rows;
	}
}
